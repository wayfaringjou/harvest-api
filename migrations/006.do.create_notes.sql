CREATE TABLE notes (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id INTEGER
    REFERENCES users(id) ON DELETE CASCADE NOT NULL,
  garden_id INTEGER
    REFERENCES gardens(id)
    ON DELETE CASCADE NOT NULL,
  area_id  INTEGER
    REFERENCES garden_areas(id)
    ON DELETE SET NULL,
  plant_id INTEGER
    REFERENCES plants(id)
    ON DELETE SET NULL,
  content TEXT,
  title VARCHAR(240)
);